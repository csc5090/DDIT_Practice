CREATE TABLE MEMBER (
    MEM_ID               NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEM_PASS           VARCHAR2(150)       NOT NULL,
    MEM_NAME          VARCHAR2(50)        NOT NULL,
    MEM_GENDER       CHAR(1) CHECK (MEM_GENDER IN ('M','F'))    NOT NULL,
    MEM_BIRTH          DATE,
    MEM_ZIP              VARCHAR2(10)        NOT NULL,
    MEM_ADD1           VARCHAR2(200)      NOT NULL,
    MEM_ADD2           VARCHAR2(200)      NOT NULL,
    MEM_HP1             VARCHAR2(3)         NOT NULL,
    MEM_HP2             VARCHAR2(4)         NOT NULL,
    MEM_HP3             VARCHAR2(4)         NOT NULL,
    MEM_MAIL            VARCHAR2(100)      NOT NULL,
    MEM_DOMAIN       VARCHAR2(50)       NOT NULL,
    GAME_NICKNAME   VARCHAR2(50)       NOT NULL,
    CREATED_DATE       DATE                   DEFAULT SYSDATE,
    DELETED_DATE       DATE,
    DELETED_REASON   VARCHAR2(200),      
    STATUS                VARCHAR2(20) DEFAULT 'ACTIVE' CHECK (STATUS IN ('ACTIVE','SUSPENDED','DELETED','PENDING','BANNED')),
    ROLE                   VARCHAR2(10) DEFAULT 'USER' CHECK (ROLE IN ('USER','ADMIN'))
);

CREATE TABLE BOARD (
    BOARD_ID         NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEM_ID            NUMBER(10)        NOT NULL,
    TITLE                VARCHAR2(200)    NOT NULL,
    CONTENT         CLOB                  NOT NULL,
    CREATED_DATE   DATE                 DEFAULT SYSDATE,
    UPDATED_DATE  DATE,
    VIEW_COUNT    NUMBER(6)          DEFAULT 0,
    CONSTRAINT FK_BOARD_MEMBER FOREIGN KEY (MEM_ID) REFERENCES MEMBER (MEM_ID)
);

CREATE TABLE NOTICE (
    NOTICE_ID        NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ADMIN_ID         NUMBER(10)      NOT NULL,
    TITLE                VARCHAR2(200)  NOT NULL,
    CONTENT         CLOB                NOT NULL,
    CREATED_DATE   DATE                DEFAULT SYSDATE,
    UPDATED_DATE  DATE,
    VIEW_COUNT    NUMBER(6)        DEFAULT 0,
    CONSTRAINT FK_NOTICE_ADMIN FOREIGN KEY (ADMIN_ID)
        REFERENCES MEMBER (MEM_ID)
);

CREATE TABLE LIBRARY (
    LIBRARY_ID         NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEM_ID              NUMBER(10)         NOT NULL,
    LIBRARY_NAME    VARCHAR2(255)     NOT NULL,
    LIBRARY_PATH     VARCHAR2(500)     NOT NULL,
    CREATED_DATE  DATE DEFAULT SYSDATE,
    DOWNLOAD_COUNT NUMBER(6) DEFAULT 0,
    CONSTRAINT FK_FILE_MEMBER FOREIGN KEY (MEM_ID) REFERENCES MEMBER (MEM_ID)
);

CREATE TABLE GAME_LOG (
    GAME_LOG_ID  NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEM_ID          NUMBER(10)    NOT NULL,
    GAME_LEVEL    VARCHAR2(20)   NOT NULL CHECK (GAME_LEVEL IN ('EASY', 'NORMAL', 'HARD', 'EXTREME')),
    SCORE            NUMBER(7)      NOT NULL,
    PLAYED_DATE   TIMESTAMP DEFAULT SYSDATE,
    CONSTRAINT FK_GAMELOG_MEMBER FOREIGN KEY (MEM_ID) REFERENCES MEMBER (MEM_ID)
);

CREATE TABLE GAME_SCORE (
    MEM_ID             NUMBER(10) PRIMARY KEY,
    SCORE_BEST       NUMBER(7)      DEFAULT 0,
    SCORE_TOTAL     NUMBER(10)     DEFAULT 0,
    SCORE_COUNT    NUMBER(6)      DEFAULT 0,
    SCORE_AVG        NUMBER(7,2)    DEFAULT 0,
    SCORE_LAST       NUMBER(7),
    PLAYED_LAST_DATE TIMESTAMP,
    CONSTRAINT FK_GAMESCORE_MEMBER FOREIGN KEY (MEM_ID) REFERENCES MEMBER (MEM_ID)
);